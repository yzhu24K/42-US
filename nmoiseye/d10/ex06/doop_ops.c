int	my_op_add(int a, int b)
{
	return (a + b);
}

int	my_op_subtract(int a, int b)
{
	return (a - b);
}

int	my_op_multiply(int a, int b)
{
	return (a * b);
}

int	my_op_divide(int a, int b)
{
	return (a / b);
}

int	my_op_modulo(int a, int b)
{
	return (a % b);
}
