#include <unistd.h>

int		main(int argc, char **argv)
{
	int		find;
	int		i;

	find = 0;
	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			if (argv[1][i] == 'a')
				find = 1;
			i++;
		}
		if (find == 1)
			write(1, "a", 1);
		write(1, "\n", 1);
	}
	else
	{
		write(1, "a", 1);
		write(1, "\n", 1);
	}
	return (0);
}
